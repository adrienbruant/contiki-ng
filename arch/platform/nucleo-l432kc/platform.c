int __io_flush(char *buffer, uint16_t len)
{
  return ( HAL_UART_Transmit(&huart2, (uint8_t*)buffer, len, 10) != HAL_OK );
}

int __io_putchar(int ch)
{
  int result = -1;

  static char buffer[STDOUT_BUF_SIZE];
  static uint16_t idx;

  if( idx < STDOUT_BUF_SIZE ) {
    buffer[idx] = ch;
  }

  if( (idx == STDOUT_BUF_SIZE) || (buffer[idx] == '\n') ) {
    if( __io_flush(buffer, idx + 1) == 0 ) {
      result = 0;
      idx = 0;
    }
  } else {
    result = 0;
    idx++;
  }

  return result;
}

int _write(int file, char *ptr, int len)
{
  int wlen;
  if( (file == STDOUT_FILENO) || (file == STDERR_FILENO) ) {
    wlen = len;
    while(wlen > 0) {
      if( *ptr == '\n' ) {
        if( __io_putchar('\r') ) {
          return -1;
        } else {
          len++;
        }
      }

      if( __io_putchar(*ptr++) ) {
        return -1;
      } else {
        wlen--;
      }
    }
  }

  return len;
}

/**
* @brief UART MSP Initialization
* This function configures the hardware resources used in this example
* @param huart: UART handle pointer
* @retval None
*/
void HAL_UART_MspInit(UART_HandleTypeDef* huart)
{
  GPIO_InitTypeDef GPIO_InitStruct = {0};
  if(huart->Instance==USART2)
  {
    /* Peripheral clock enable */
    __HAL_RCC_USART2_CLK_ENABLE();
  
    __HAL_RCC_GPIOA_CLK_ENABLE();
    /**USART2 GPIO Configuration    
    PA2     ------> USART2_TX
    PA15 (JTDI)     ------> USART2_RX 
    */
    GPIO_InitStruct.Pin = VCP_TX_Pin;
    GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
    GPIO_InitStruct.Alternate = GPIO_AF7_USART2;
    HAL_GPIO_Init(VCP_TX_GPIO_Port, &GPIO_InitStruct);

    GPIO_InitStruct.Pin = VCP_RX_Pin;
    GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
    GPIO_InitStruct.Alternate = GPIO_AF3_USART2;
    HAL_GPIO_Init(VCP_RX_GPIO_Port, &GPIO_InitStruct);
  }
}

/**
* @brief UART MSP De-Initialization
* This function freeze the hardware resources used in this example
* @param huart: UART handle pointer
* @retval None
*/
void HAL_UART_MspDeInit(UART_HandleTypeDef* huart)
{
  if(huart->Instance==USART2)
  {
    /* Peripheral clock disable */
    __HAL_RCC_USART2_CLK_DISABLE();
  
    /**USART2 GPIO Configuration    
    PA2     ------> USART2_TX
    PA15 (JTDI)     ------> USART2_RX 
    */
    HAL_GPIO_DeInit(GPIOA, VCP_TX_Pin|VCP_RX_Pin);
  }
}